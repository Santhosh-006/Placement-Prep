

public class RightTriangle {
        public static void main(String[] args) {
            int n=5;
            int i;
            for(int j=0 ; j<n ;j++){
                i=j+1;
                while(i>0){
                        System.out.print("*");
                        i--;
                }
                System.out.println("");
            }
        }
}
